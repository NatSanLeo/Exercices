public class Descendente {
    public static void main(String[] args) {
        int cincuenta = 50;

        for (int y = 0; y <= cincuenta; y++) {
            System.out.print(cincuenta - y + " ");
        }
        System.out.println();

        for (int u = cincuenta; u >= 0; u--) {
            System.out.print(u + " ");
        }

    }
}
